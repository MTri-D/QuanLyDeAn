CREATE TABLE NHANVIEN (
  MANV NUMBER(10) CONSTRAINT PK_NHANVIEN PRIMARY KEY,
  TENNV VARCHAR2(50) NOT NULL,
  PHAI VARCHAR2(10),
  NGAYSINH DATE,
  DIACHI VARCHAR2(100),
  SODT VARCHAR2(20),
  LUONG NUMBER(18,2),
  PHUCAP NUMBER(18,2),
  VAITRO VARCHAR2(50),
  MANQL NUMBER(10),
  PHG VARCHAR2(50)
  --CONSTRAINT FK_MANQL FOREIGN KEY(MANQL) REFERENCES NHANVIEN(MANV),
  --CONSTRAINT FK_PHG FOREIGN KEY(PHG) REFERENCES PHONGBAN(TENPB)
);


CREATE TABLE PHONGBAN (
  MAPB VARCHAR2(10) CONSTRAINT PK_PHONGBAN PRIMARY KEY,
  TENPB VARCHAR2(50) NOT NULL,
  TRPHG NUMBER(10)
  --CONSTRAINT FK_PHONGBAN_NHANVIEN FOREIGN KEY(TRPHG) REFERENCES NHANVIEN(MANV)
);


CREATE TABLE DEAN(
  MADA NUMBER(3) CONSTRAINT PK_DEAN PRIMARY KEY,
  TENDA VARCHAR2(100) UNIQUE,
  NGAYBD DATE,
  PHONG VARCHAR2(10)
  --CONSTRAINT FK_DEAN_PHONGBAN FOREIGN KEY(PHONG) REFERENCES PHONGBAN(MAPB)
);


CREATE TABLE PHANCONG (
  MANV NUMBER(10) CONSTRAINT FK_PHANCONG_NHANVIEN REFERENCES NHANVIEN(MANV),
  MADA NUMBER(3) CONSTRAINT FK_PHANCONG_DEAN REFERENCES DEAN(MADA),
  THOIGIAN DATE
  --CONSTRAINT PK_PHANCONG PRIMARY KEY(MANV,MADA)
);



  